package com.aftermath.pojo;

public class Flow {
    private String courseId;

    private String courseName;

    private String supervisorInstructor;

    private String teacherInstructor;

    public String getCourseId() {
        return courseId;
    }

    public void setCourseId(String courseId) {
        this.courseId = courseId;
    }

    public String getCourseName() {
        return courseName;
    }

    public void setCourseName(String courseName) {
        this.courseName = courseName;
    }

    public String getSupervisorInstructor() {
        return supervisorInstructor;
    }

    public void setSupervisorInstructor(String supervisorInstructor) {
        this.supervisorInstructor = supervisorInstructor;
    }

    public String getTeacherInstructor() {
        return teacherInstructor;
    }

    public void setTeacherInstructor(String teacherInstructor) {
        this.teacherInstructor = teacherInstructor;
    }
}
